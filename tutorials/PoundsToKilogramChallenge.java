package learning.tutorials;

public class PoundsToKilogramChallenge {
    public static void main(String[] args)
    {
        double pounds = 200d;
        double poundsToKilograms = .45359237d * pounds;
        System.out.println(pounds + " pounds is equal to " + poundsToKilograms + " kilograms.");
    }
}
